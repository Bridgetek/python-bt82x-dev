def eve_load_images(eve, start_addr, handle):
    global img_bridgetek_logo
    global eve_img_bridgetek_logo_width
    global eve_img_bridgetek_logo_height

    while(len(img_bridgetek_logo) & 3): img_bridgetek_logo += bytes([0])
    
    eve.LIB_BeginCoProList()
    eve.CMD_DLSTART()
    eve.BEGIN(eve.BEGIN_BITMAPS)
    eve.BITMAP_HANDLE(handle)
    eve.CMD_LOADIMAGE(start_addr, 0)
    # Send raw JPEG encoded image data to coprocessor. It will be decoded
    # as the data is received.
    eve.LIB_WriteDataToCMD(img_bridgetek_logo)
    eve.DISPLAY()
    eve.CMD_SWAP()
    eve.LIB_EndCoProList()
    eve.LIB_AwaitCoProEmpty()

    _, _, eve_img_bridgetek_logo_width, eve_img_bridgetek_logo_height, _ = eve.LIB_GetImage()

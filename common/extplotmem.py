# Align an array to a multiple of 4 bytes (32 bits).
# Required for writing block data to EVE.
def pad4(s):
    while len(s) % 4:
        s += b'\x00'
    return s

extplotmem = pad4(extplotmem)

def loadpatch(eve):
    eve.LIB_BEGINCOPROLIST()
    eve.CMD_DLSTART()
    eve.CMD_LOADPATCH(0)
    eve.LIB_WRITEDATATOCMD(extplotmem)
    eve.LIB_ENDCOPROLIST()
    eve.LIB_AWAITCOPROEMPTY()
    versions = eve.LIB_GETCOPROEXCEPTION()
    return (versions)
